# Ejercicio 02:
#   Hacer un programa donde se solicite por teclado
#   dos numeros y se imprima la suma, resta, multiplicacion y division

print('Ingrese un numero:')
numb_a = int(input())
print('Ingrese otro numero:')
numb_b = int(input())

print(f'suma: a + b = {numb_a + numb_b}')
print(f'resta: a - b = {numb_a - numb_b}')
print(f'multiplicacion: a * b = {numb_a * numb_b}')
print(f'division: a / b = {numb_a / numb_b}')